package hash_set;

public class Aluno {
	public String nome;
	public String materiaEstudada;
	
	public Aluno(String nome, String materiaEstudada) {
		super();
		this.nome = nome;
		this.materiaEstudada = materiaEstudada;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getMateriaEstudada() {
		return materiaEstudada;
	}

	public void setMateriaEstudada(String materiaEstudada) {
		this.materiaEstudada = materiaEstudada;
	}
	
	
	
}
